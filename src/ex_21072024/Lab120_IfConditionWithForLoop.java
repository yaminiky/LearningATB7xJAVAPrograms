package ex_21072024;

public class Lab120_IfConditionWithForLoop {
    public static void main(String[] args) {
        for(int i=1; i<=10;i++)
        {
            if (i==5)
            {
                System.out.println("FIVE");
            }else
            {
                System.out.println(i);
            }
        }
    }
}
